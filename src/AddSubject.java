    private void ConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ConfirmActionPerformed
String name   = Subject.getText().trim();
String code   = Code.getText().trim();
String units  = Units.getText().trim();
String creditsText = Credits.getText().trim();

if (name.isEmpty() || code.isEmpty()) {
    JOptionPane.showMessageDialog(this, "Subject Name and Code are required.", "Missing Info", JOptionPane.WARNING_MESSAGE);
    return;
}

int credits;
try {
    credits = creditsText.isEmpty() ? 0 : Integer.parseInt(creditsText);
} catch (NumberFormatException ex) {
    JOptionPane.showMessageDialog(this, "Credits must be a number.", "Invalid Input", JOptionPane.WARNING_MESSAGE);
    return;
}

try (Connection con = Connector.getConnection()) {
    // Check for duplicate SubjectCode
    String checkSQL = "SELECT COUNT(*) FROM subject WHERE SubjectCode = ?";
    PreparedStatement checkPst = con.prepareStatement(checkSQL);
    checkPst.setString(1, code);
    ResultSet rs = checkPst.executeQuery();
    if (rs.next() && rs.getInt(1) > 0) {
        JOptionPane.showMessageDialog(this, "Subject Code already exists.", "Duplicate", JOptionPane.WARNING_MESSAGE);
        return;
    }

    // Insert subject
    String insertSQL = "INSERT INTO subject (SubjectName, SubjectCode, Units, Credits) VALUES (?, ?, ?, ?)";
    PreparedStatement pst = con.prepareStatement(insertSQL);
    pst.setString(1, name);
    pst.setString(2, code);
    pst.setString(3, units);
    pst.setInt(4, credits);
    pst.executeUpdate();

    JOptionPane.showMessageDialog(this, "Subject created successfully!");
    dispose(); // close dialog

} catch (SQLException ex) {
    JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage(), "Insert Failed", JOptionPane.ERROR_MESSAGE);
    ex.printStackTrace();
}    // TODO add your handling code here:
    }//GEN-LAST:event_ConfirmActionPerformed

