    private void ConfirmDelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ConfirmDelActionPerformed

    String selected = assessmentComboBox.getSelectedItem().toString();
    int assessmentID;

    try {
        assessmentID = Integer.parseInt(selected.split(" - ")[0]); // extract ID
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, "Invalid selection.");
        return;
    }

    int confirm = JOptionPane.showConfirmDialog(this,
        "Are you sure you want to delete Assessment ID " + assessmentID + "?",
        "Confirm Deletion",
        JOptionPane.YES_NO_OPTION);

    if (confirm != JOptionPane.YES_OPTION) return;

    try (Connection con = Connector.getConnection()) {
        String deleteSQL = "DELETE FROM assessment WHERE AssessmentID = ?";
        PreparedStatement pst = con.prepareStatement(deleteSQL);
        pst.setInt(1, assessmentID);
        int affected = pst.executeUpdate();

        if (affected > 0) {
            JOptionPane.showMessageDialog(this, "Assessment deleted successfully!");
            dispose(); // close dialog
        } else {
            JOptionPane.showMessageDialog(this, "No assessment was deleted.");
        }

    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Error deleting assessment: " + ex.getMessage());
        ex.printStackTrace();
    }
        // TODO add your handling code here:
    }//GEN-LAST:event_ConfirmDelActionPerformed

