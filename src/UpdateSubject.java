private void populateSubjectComboBox() {
    subjectComboBox.removeAllItems();
    try (Connection con = Connector.getConnection()) {
        String sql = "SELECT SubjectID, SubjectCode FROM subject";
        PreparedStatement pst = con.prepareStatement(sql);
        ResultSet rs = pst.executeQuery();
        while (rs.next()) {
            int id = rs.getInt("SubjectID");
            String code = rs.getString("SubjectCode");
            subjectComboBox.addItem(id + " - " + code);
        }
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Error loading subjects: " + ex.getMessage());
        ex.printStackTrace();
    }
}
