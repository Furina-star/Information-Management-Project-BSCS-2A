    private void UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateActionPerformed

    String selected = subjectComboBox.getSelectedItem().toString();
    int subjectID;
    try {
        subjectID = Integer.parseInt(selected.split(" - ")[0].trim());
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, "Invalid subject selection.");
        return;
    }

    String name = nameField.getText().trim();
    String code = codeField.getText().trim();
    String units = unitsField.getText().trim();
    String creditsText = creditsField.getText().trim();

    if (name.isEmpty() || code.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Subject Name and Code are required.");
        return;
    }

    int credits;
    try {
        credits = creditsText.isEmpty() ? 0 : Integer.parseInt(creditsText);
    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(this, "Credits must be a number.");
        return;
    }

    try (Connection con = Connector.getConnection()) {
        String updateSQL = "UPDATE subject SET SubjectName = ?, SubjectCode = ?, Units = ?, Credits = ? WHERE SubjectID = ?";
        PreparedStatement pst = con.prepareStatement(updateSQL);
        pst.setString(1, name);
        pst.setString(2, code);
        pst.setString(3, units);
        pst.setInt(4, credits);
        pst.setInt(5, subjectID);
        pst.executeUpdate();

        JOptionPane.showMessageDialog(this, "Subject updated successfully!");
        dispose();
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Update failed: " + ex.getMessage());
        ex.printStackTrace();
    }

    }//GEN-LAST:event_UpdateActionPerformed

