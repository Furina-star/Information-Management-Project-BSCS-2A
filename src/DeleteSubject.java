    private void ConfirmDeletionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ConfirmDeletionActionPerformed

    int subjectID = Integer.parseInt(Subject.getSelectedItem().toString().split(" - ")[0].trim());

try (Connection con = Connector.getConnection()) {
    // Remove links first
    String unlinkSQL = "DELETE FROM student_subject WHERE SubjectID = ?";
    PreparedStatement unlinkPst = con.prepareStatement(unlinkSQL);
    unlinkPst.setInt(1, subjectID);
    unlinkPst.executeUpdate();

    // Then delete subject
    String deleteSQL = "DELETE FROM subject WHERE SubjectID = ?";
    PreparedStatement deletePst = con.prepareStatement(deleteSQL);
    deletePst.setInt(1, subjectID);
    deletePst.executeUpdate();

    JOptionPane.showMessageDialog(this, "Subject deleted successfully!");
    Subject.removeAllItems();
        PopulateCbSub(); // or whatever method you use to reload subjects
} catch (SQLException ex) {
    JOptionPane.showMessageDialog(this, "Delete failed: " + ex.getMessage());
    ex.printStackTrace();
}
    }//GEN-LAST:event_ConfirmDeletionActionPerformed

